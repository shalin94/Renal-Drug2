package com.renaldrug2;

public class Medicine {
String name;
String type;
public Medicine(String name){
	super();
	this.name = name;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getType() {
	return type;
}
public void setType(String type) {
	this.type = type;
}
}
